#include "../../includes/so_long.h"

void	ft_putnbr(int n)
{
	int	i;

	i = 0;
	if (n > 9)
	{
		i = i + (n % 10);
		ft_putnbr(n / 10);
		i = i + '0';
		write(1, &i, 1);
		return ;
	}
	else
	{
		n += '0';
		write(1, &n, 1);
	}
}
